#Numbers: integers, float, complex
#Integers; whole numbers(+ve/-ve)
x=2000
y=77
z=-540
print(type(x))
print(type(y))
print(type(z))
#Float - numbers with dp
a=10.45
b=99.99
print(type(a))
print(type(b))
#Complex; numbers with unknown part
#j is used to show the imaginary part
c=2+5j
d=6j+3
print(type(c))
print(type(d))
#Type conversion float(), int(), complex()
#From integers to float
e=37
mynum=float(e)
print(mynum)
print(type(mynum))
#convert from float to int
my=19.45
new=int(my)
print(new)
print(type(new))
#Convert int to complex
f=645
wa=complex(f)
print(wa)
print(type(wa))
